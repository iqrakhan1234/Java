public class Forloop1 {
    public static void main(String[] args) {
        int i;
        for (i = 1; i <= 5; i++)
            System.out.println(i);
        // we used ; after for loop so the o/p will be 6
        // bcoz it terminates at 6
        // if we do not use ; after for loop the o/p will be
        // 1
        // 2
        // 3
        // 4
        // 5

    }
}
